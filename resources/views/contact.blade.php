<script>
    document.getElementById("contact-form").addEventListener("submit", function (event) {
        event.preventDefault();
        // Get input and error span elements
        const firstInput = document.getElementById("first");
        const lastInput = document.getElementById("last_name");
        const emailInput = document.getElementById("email");
        const phoneInput = document.getElementById("phone");
        const detailsInput = document.getElementById("details");

        const firstError = document.getElementById("first-error");
        const lastError = document.getElementById("last-error");
        const emailError = document.getElementById("email-error");
        const phoneError = document.getElementById("phone-error");
        const detailsError = document.getElementById("details-error");

        // Clear previous error messages
        firstError.textContent = "";
        lastError.textContent = "";
        emailError.textContent = "";
        phoneError.textContent = "";
        detailsError.textContent = "";

        // Validate required fields
        if (firstInput.value.trim() === "") {
            firstError.textContent = "Förnamn är obligatoriskt.";
            return;
        }
        if (lastInput.value.trim() === "") {
            lastError.textContent = "Efternamn är obligatoriskt.";
            return;
        }
        const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailRegex.test(emailInput.value.trim())) {
            emailError.textContent = "Ange en giltig e-postadress.";
            return;
        }
        if (detailsInput.value.trim() === "") {
            detailsError.textContent = "Detaljer är obligatoriskt.";
            return;
        }
        const phoneRegex = /^\+?[0-9\s\-()]{7,}$/;
        if (phoneInput.value.trim() !== "" && !phoneRegex.test(phoneInput.value.trim())) {
            phoneError.textContent = "Ange ett giltigt telefonnummer.";
            return;
        }

        // Prepare AJAX submission
        const form = document.getElementById("contact-form");
        const formData = new FormData(form);
        const xhr = new XMLHttpRequest();
        xhr.open("POST", form.action, true);
        xhr.setRequestHeader("Accept", "application/json");

        xhr.onload = function () {
            if (xhr.status >= 200 && xhr.status < 300) {
                // Clear form fields manually
                firstInput.value = "";
                lastInput.value = "";
                emailInput.value = "";
                phoneInput.value = "";
                detailsInput.value = "";
                // Show SweetAlert and redirect after confirmation
                Swal.fire("Tack!", "Din förfrågan har skickats. Vi återkommer snart.", "success")
                    .then((result) => {
                        window.location.href = "index.html";
                    });
            } else {
                Swal.fire("Fel!", "Något gick fel. Var vänlig försök igen.", "error");
            }
        };

        xhr.send(formData);
    });
</script>
